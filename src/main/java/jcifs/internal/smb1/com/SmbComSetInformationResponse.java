package jcifs.internal.smb1.com;

import jcifs.Configuration;
import jcifs.internal.smb1.ServerMessageBlock;

/**
 * Response for SMB1 COM_SET_INFORMATION command.
 *
 * This response indicates the success or failure of setting file
 * attributes and last write time.
 *
 * @author mbechler
 */
public class SmbComSetInformationResponse extends ServerMessageBlock {

    /**
     * Constructs a set information response.
     *
     * @param config the configuration to use
     */
    public SmbComSetInformationResponse(final Configuration config) {
        super(config);
    }

    @Override
    protected int writeParameterWordsWireFormat(final byte[] dst, final int dstIndex) {
        return 0;
    }

    @Override
    protected int writeBytesWireFormat(final byte[] dst, final int dstIndex) {
        return 0;
    }

    @Override
    protected int readParameterWordsWireFormat(final byte[] buffer, final int bufferIndex) {
        return 0;
    }

    @Override
    protected int readBytesWireFormat(final byte[] buffer, final int bufferIndex) {
        return 0;
    }

    @Override
    public String toString() {
        return ("SmbComSetInformationResponse[" + super.toString() + "]");
    }
}
